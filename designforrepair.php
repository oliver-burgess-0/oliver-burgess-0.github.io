            <div class="paragraph col-xs-12 col-sm-12 col-md-8 col-md-offset-2"><p>Repair has become an increasingly forgotten art; items that were traditionally mended are treated as disposable in today&#8217;s society. In most cases, the cost and effort associated with diagnosing and remedying faults presents repair as an unattractive option. The aim of the project was to analyse the current state of Design for Repair in household appliances, and develop a set of Design for Repair guidelines to be used when designing both product and repair service.</p></div>
            <div class="paragraph col-xs-12 col-sm-12 col-md-8 col-md-offset-2"><p>Having analysed the current consideration for the repair of a household appliance at its design stage, a toolkit has been developed to try and increase this consideration. The barriers to repair can be identified by breaking the process down into: diagnosis, disassembly and part replacement. Each category contains five criteria that provide a framework for scoring a given fault, based on the ease of its remediation.</p></div>
            <div class="heading col-xs-12 col-sm-12 col-md-8 col-md-offset-2"><h1>design products where critical faults are easily and specifically diagnosable.</h1></div>
            <div class="clear col-xs-8 col-sm-8 col-md-3 col-md-offset-2"><img class="project-image" src="diagnosissector.png"/></div>
            <div class="list col-xs-12 col-sm-12 col-md-4 col-md-offset-1">   
                <ol>
                    <li>Employ use of visuals to aid fault diagnosis, e.g. through using status lights</li><br>
                    <li>Ensure faults exhibit obvious symptoms that can be identified by our senses</li><br>
                    <li>Provide extensive documentation listing all failure modes and troubleshooting steps required to diagnose fault</li><br>
                    <li>Integrate sensors that aid diagnosis e.g. temperature/current cut-outs</li>
                </ol>
            </div>
            <div class="caption col-xs-12 col-sm-12 col-md-8 col-md-offset-2"><p>Fault diagnosis guidelines</p></div>
            <div class="heading col-xs-12 col-sm-12 col-md-8 col-md-offset-2"><h1>design products that are easily disassembled and reassembled without requiring specialist skills or equipment.</h1></div>
            <div class="clear col-xs-8 col-sm-8 col-md-3 col-md-offset-2"><img class="project-illustration-b" src="disassemblysector.png"/></div>
            <div class="list col-xs-12 col-sm-12 col-md-4 col-md-offset-1"> 
                <ol>
                    <li>Minimise the number of steps in disassembling a product</li><br>
                    <li>Avoid any intricate operations or ones that require specialist skill</li><br>
                    <li>Ensure only common, household tools are required</li><br>
                    <li>Use only reversible fastenings during assembly of product</li>
                </ol>
            </div>
            <div class="caption col-xs-12 col-sm-12 col-md-8 col-md-offset-2"><p>Product disassembly guidelines</p></div>
            <div class="heading col-xs-12 col-sm-12 col-md-8 col-md-offset-2"><h1>design products where replacement parts are easy to source and inexpensive.</h1></div>
            <div class="clear col-xs-8 col-sm-8 col-md-3 col-md-offset-2"><img class="project-image" src="partreplacement.png"/></div>
            <div class="list col-xs-12 col-sm-12 col-md-4 col-md-offset-1">     
                <ol>
                    <li>Ensure all replacement parts are easy to source via the manufacturer&#8217;s website and in stock throughout a product&#8217;s life</li><br>
                    <li>The cost of a replacement part should be small compared to the original price of the product</li><br>
                    <li>Individual parts should be made available rather than subassemblies</li><br>
                    <li>A system for recovering faulty parts and reusing or recycling them should be in place</li>
                </ol>
            </div>
